package org.example.selfcode.cwhCode.Chapter.chp01VariablesDatatype;

public class literals_04 {

    public static void main(String[] args) {
        byte age = 34;
        System.out.print(age);
        String str = "Harry";
        System.out.println(str);

    }

    /**
    In Java, literals are the fixed values you write directly in the code that represent data.
    They are constant values assigned to variables or used in expressions.

    Types of Literals in Java
    Here are the different types of literals with examples:

    Integer Literals

    Whole numbers without a decimal.
            Example:
    int age = 25; // Decimal literal
    int hex = 0x1A; // Hexadecimal (prefix 0x)
    int binary = 0b1101; // Binary (prefix 0b)
    Floating-Point Literals

    Numbers with decimals or exponential notation.
    Example:
    float price = 19.99f; // Suffix 'f' for float
    double distance = 1234.56; // Double by default
    Character Literals

    A single character enclosed in single quotes.
            Example:
    char grade = 'A';
    char symbol = '#';
    String Literals

    A sequence of characters enclosed in double quotes.
    Example:
    String name = "Hello, Java!";
    Boolean Literals

    Represent true or false.
    Example:
    boolean isActive = true;
    Null Literal

    Represents the absence of a value for reference types.
    Example:
    String text = null;
    Key Points:
    Literals are the simplest form of values in Java.
    They are directly written in the code and cannot be changed during program execution.
    Java provides syntax for different bases (decimal, hexadecimal, binary) and types (int, float, etc.).
     */
}

